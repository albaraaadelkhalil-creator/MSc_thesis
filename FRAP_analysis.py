def ask_yes_no(question, default="n"):
    reply = input(f"{question} [{'Y/n' if default.lower() == 'y' else 'y/N'}]: ").strip().lower()
    if reply == "":
        reply = default.lower()
    return reply in ["y", "yes"]

def ask_optional_float(question, default=None):
    """
    Ask for an optional floating-point value.
    Pressing Enter returns the default.
    """
    default_text = "Enter" if default is None else str(default)
    reply = input(f"{question} [{default_text}]: ").strip()

    if reply == "":
        return default

    try:
        return float(reply)
    except ValueError:
        raise ValueError(f"Expected a number or Enter, but got: {reply}")


def configure_fit_window_from_time(time_axis, default_start_index, default_end_index=None):
    """
    Convert a user-selected fitting time window in seconds into start/end indices.

    The end index follows normal Python slicing conventions: it is exclusive.
    If the user presses Enter for both questions, the default index-based window is used.
    """
    time_axis = np.asarray(time_axis, dtype=float)

    if default_end_index is None:
        default_end_index = len(time_axis)

    default_start_time = float(time_axis[default_start_index])
    default_end_time = float(time_axis[default_end_index - 1])

    if not ASK_FOR_FIT_TIME_WINDOW:
        return default_start_index, default_end_index

    print("\n" + "=" * 80)
    print("Fit time-window selection")
    print("=" * 80)
    print(f"Default fit window: index {default_start_index} to {default_end_index - 1}")
    print(f"Default fit window: {default_start_time:.3f} s to {default_end_time:.3f} s")
    print("Press Enter for both questions to keep the default full postbleach window.")

    start_time = ask_optional_float(
        "Fit start time in seconds",
        default=default_start_time,
    )
    end_time = ask_optional_float(
        "Fit end time in seconds",
        default=default_end_time,
    )

    if start_time is None:
        start_time = default_start_time
    if end_time is None:
        end_time = default_end_time

    if end_time <= start_time:
        raise ValueError("Fit end time must be larger than fit start time.")

    fit_start_index = int(np.searchsorted(time_axis, start_time, side="left"))
    fit_end_index = int(np.searchsorted(time_axis, end_time, side="right"))

    fit_start_index = max(0, min(fit_start_index, len(time_axis) - 2))
    fit_end_index = max(fit_start_index + 2, min(fit_end_index, len(time_axis)))

    print(
        f"Using fit window: index {fit_start_index} to {fit_end_index - 1} "
        f"({time_axis[fit_start_index]:.3f} s to {time_axis[fit_end_index - 1]:.3f} s)"
    )
    print("=" * 80 + "\n")

    return fit_start_index, fit_end_index


def build_time_axis_from_original_blocks(time_blocks, data_length, bin_size=3):
    """
    Build the correct time axis when TIME_BLOCKS contains original row counts,
    but each time point is produced after binning by bin_size.

    Example:
        ("Pb3", 90, 10.0)

    means:
        90 original rows / 3 = 30 actual binned time points,
        with 10.0 s between those binned time points.

    Therefore Pb3 contributes ~30 time points, not 90 time points.
    """
    binned_times = []
    current_time = 0.0

    total_original_rows = sum(n_rows for _, n_rows, _ in time_blocks)

    if total_original_rows % bin_size != 0:
        raise ValueError(
            f"Total original rows {total_original_rows} is not divisible by bin_size={bin_size}"
        )

    expected_binned_length = total_original_rows // bin_size

    if data_length != expected_binned_length:
        raise ValueError(
            f"Data length is {data_length}, but expected {expected_binned_length} "
            f"after binning {total_original_rows} original rows by bin_size={bin_size}. "
            f"Check TIME_BLOCKS or BIN_SIZE."
        )

    for block_name, n_original_rows, dt in time_blocks:
        if n_original_rows % bin_size != 0:
            raise ValueError(
                f"Block {block_name} has {n_original_rows} original rows, "
                f"which is not divisible by bin_size={bin_size}"
            )

        n_binned_points = n_original_rows // bin_size

        for _ in range(n_binned_points):
            binned_times.append(current_time)
            current_time += dt

    time_axis = np.array(binned_times, dtype=float)

    print("\n" + "=" * 80)
    print("Time axis check")
    print("=" * 80)
    print(f"Number of time points       : {len(time_axis)}")
    print(f"First plotted time          : {time_axis[0]:.3f} s")
    print(f"Last plotted time           : {time_axis[-1]:.3f} s")
    print(f"Estimated acquisition end   : {current_time:.3f} s")
    print("=" * 80 + "\n")

    return time_axis


def bin_trace(y, bin_size=3):
    y = np.asarray(y, dtype=float)

    if len(y) % bin_size != 0:
        raise ValueError(f"Trace length {len(y)} is not divisible by bin_size={bin_size}")

    return y.reshape(-1, bin_size).mean(axis=1)


def expected_analysis_length(time_blocks, bin_size):
    """
    Expected number of plotted/analyzed time points after binning.
    For your current TIME_BLOCKS:
        180 original rows / 3 = 60 analyzed points
    """
    total_original_rows = sum(n_rows for _, n_rows, _ in time_blocks)

    if total_original_rows % bin_size != 0:
        raise ValueError(
            f"Total original rows {total_original_rows} is not divisible by bin_size={bin_size}"
        )

    return total_original_rows // bin_size


def maybe_bin_trace(y, bin_size=3, time_blocks=None, csv_path=None):
    """
    Accepts either:
    - original traces with 180 rows, which are binned to 60
    - already-binned traces with 60 rows, which are kept unchanged

    This prevents Linear files with 60 rows from being rejected.
    """
    y = np.asarray(y, dtype=float)

    if time_blocks is None:
        raise ValueError("time_blocks must be provided to maybe_bin_trace().")

    expected_original_len = sum(n_rows for _, n_rows, _ in time_blocks)
    expected_binned_len = expected_analysis_length(time_blocks, bin_size)

    filename = os.path.basename(csv_path) if csv_path is not None else "trace"

    if len(y) == expected_original_len:
        print(f"Binning {filename}: {len(y)} rows -> {expected_binned_len} points")
        return bin_trace(y, bin_size=bin_size)

    elif len(y) == expected_binned_len:
        print(f"Keeping {filename}: already {expected_binned_len} points")
        return y

    else:
        raise ValueError(
            f"{filename} has {len(y)} rows. Expected either "
            f"{expected_original_len} original rows or {expected_binned_len} already-binned rows."
        )


def get_trace_from_csv(csv_path):
    """
    Read the first two columns of the CSV:
    first column = frame
    second column = intensity

    Returns only the intensity values for processing.
    """
    df = pd.read_csv(csv_path)

    if df.shape[1] < 2:
        raise ValueError(f"File has fewer than 2 columns:\n{csv_path}")

    frame = pd.to_numeric(df.iloc[:, 0], errors="coerce").to_numpy(dtype=float)
    intensity = pd.to_numeric(df.iloc[:, 1], errors="coerce").to_numpy(dtype=float)

    valid = np.isfinite(frame) & np.isfinite(intensity)
    intensity = intensity[valid]

    if len(intensity) == 0:
        raise ValueError(f"No valid intensity values found in file:\n{csv_path}")

    return intensity


def normalize_to_prebleach(y, prebleach_points=5, flatten_prebleach=True):
    """
    Normalize trace by prebleach mean and optionally flatten prebleach region.
    """
    y = np.asarray(y, dtype=float)

    if len(y) < prebleach_points:
        raise ValueError(
            f"Trace length {len(y)} is shorter than PREBLEACH_POINTS={prebleach_points}"
        )

    baseline = np.nanmean(y[:prebleach_points])

    if np.isnan(baseline) or np.isclose(baseline, 0):
        raise ValueError("Prebleach baseline is zero or NaN")

    y_norm = y / baseline

    # Force prebleach to exactly 1
    if flatten_prebleach:
        y_norm[:prebleach_points] = 1.0

    return y_norm

def reference_correct_and_normalize(
    bleached_raw,
    reference_raw,
    background_raw,
    prebleach_points=5,
    flatten_prebleach=True,
    eps=1e-9,
):
    """
    Background-correct, reference-correct, and prebleach-normalize a FRAP trace.

    Correction:
        corrected(t) = [bleached_raw(t) - background(t)] /
                       [reference_raw(t) - background(t)]

    Then:
        normalized(t) = corrected(t) / mean(corrected prebleach)

    This corrects the bleached ROI for acquisition bleaching or global intensity
    changes observed in the non-bleached reference ROI.
    """
    bleached_raw = np.asarray(bleached_raw, dtype=float)
    reference_raw = np.asarray(reference_raw, dtype=float)
    background_raw = np.asarray(background_raw, dtype=float)

    bleached_bg = bleached_raw - background_raw
    reference_bg = reference_raw - background_raw

    # Avoid division by zero or negative unstable reference values
    reference_bg_safe = np.where(
        np.abs(reference_bg) < eps,
        np.nan,
        reference_bg
    )

    corrected = bleached_bg / reference_bg_safe

    if len(corrected) < prebleach_points:
        raise ValueError(
            f"Trace length {len(corrected)} is shorter than "
            f"prebleach_points={prebleach_points}"
        )

    baseline = np.nanmean(corrected[:prebleach_points])

    if np.isnan(baseline) or np.isclose(baseline, 0):
        raise ValueError("Reference-corrected prebleach baseline is zero or NaN")

    corrected_norm = corrected / baseline

    if flatten_prebleach:
        corrected_norm[:prebleach_points] = 1.0

    return corrected_norm

def compute_error(data_2d, error_type="sem"):
    data_2d = np.asarray(data_2d, dtype=float)
    mean = np.nanmean(data_2d, axis=0)

    if error_type.lower() == "std":
        err = np.nanstd(data_2d, axis=0, ddof=1)
    elif error_type.lower() == "sem":
        n = np.sum(~np.isnan(data_2d), axis=0)
        std = np.nanstd(data_2d, axis=0, ddof=1)
        err = std / np.sqrt(np.maximum(n, 1))
    else:
        raise ValueError("ERROR_TYPE must be 'sem' or 'std'")

    return mean, err


def parse_filename(filepath):
    """
    Parse filenames like:
    26.02.06.B1_001_background.csv
    26.02.06.B2_001_bleached_cortex.csv
    26.02.06.L1_001_bleached_Lumen.csv
    26.02.06.L2_001_ref_cortex.csv
    """
    filename = os.path.basename(filepath)
    stem = os.path.splitext(filename)[0]

    pattern = re.compile(
        r"^(?P<date>\d{2}\.\d{2}\.\d{2})\.(?P<group>B1|B2|L1|L2)_(?P<number>\d+)_(?P<role>.+)$"
    )

    match = pattern.match(stem)
    if not match:
        raise ValueError(f"Filename not recognized:\n{filename}")

    date_str = match.group("date")
    group_code = match.group("group").upper()
    number_str = match.group("number")
    role_raw = match.group("role")

    role = role_raw.lower()

    if role == "background":
        role = "background"
    elif role == "bleached_cortex":
        role = "bleached_cortex"
    elif role == "bleached_lumen":
        role = "bleached_lumen"
    elif role == "ref_cortex":
        role = "ref_cortex"
    elif role == "ref_lumen":
        role = "ref_lumen"
    else:
        raise ValueError(f"Unknown role '{role_raw}' in file:\n{filename}")

    if group_code in ["B1", "B2"]:
        group_name = "Branched"
    elif group_code in ["L1", "L2"]:
        group_name = "Linear"
    else:
        raise ValueError(f"Unknown group code '{group_code}' in file:\n{filename}")

    return date_str, group_code, group_name, number_str, role


