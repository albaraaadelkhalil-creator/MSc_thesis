def _legend_output_path(plot_path):
    """Return the PNG path used for the separate legend."""
    root, _ = os.path.splitext(str(plot_path))
    return root + "_legend.png"


def save_legend_figure(handles, labels, plot_path, ncol=LEGEND_MAX_COLUMNS):
    """Save a de-duplicated 16-point bold legend as a separate file."""
    if not SAVE_LEGENDS_SEPARATELY or plot_path is None:
        return

    unique_handles = []
    unique_labels = []
    seen = set()

    for handle, label in zip(handles, labels):
        label = str(label)
        if not label or label.startswith("_") or label in seen:
            continue
        unique_handles.append(handle)
        unique_labels.append(label)
        seen.add(label)

    if not unique_handles:
        return

    ncol = min(max(1, ncol), len(unique_handles))
    nrows = int(np.ceil(len(unique_handles) / ncol))
    longest = max(len(label) for label in unique_labels)
    width = max(8.0, min(18.0, 2.5 * ncol + 0.05 * longest))
    height = max(1.8, 0.7 + 0.65 * nrows)

    legend_fig = plt.figure(figsize=(width, height))
    legend = legend_fig.legend(
        unique_handles,
        unique_labels,
        loc="center",
        frameon=False,
        ncol=ncol,
        handlelength=2.8,
        columnspacing=1.5,
        handletextpad=0.7,
        prop={"size": LEGEND_FONT_SIZE, "weight": "bold"},
    )
    for item in legend.get_texts():
        item.set_fontsize(LEGEND_FONT_SIZE)
        item.set_fontweight("bold")

    legend_fig.tight_layout()
    legend_png = _legend_output_path(plot_path)
    legend_fig.savefig(
        legend_png,
        dpi=STATIC_PLOT_DPI,
        bbox_inches="tight",
        transparent=True,
    )

    if SAVE_LEGEND_PDF:
        legend_pdf = os.path.splitext(legend_png)[0] + ".pdf"
        legend_fig.savefig(
            legend_pdf,
            bbox_inches="tight",
            transparent=True,
        )

    plt.close(legend_fig)
    print(f"Separate legend saved:\n{legend_png}")


def save_current_plot_without_legend(save_path, ncol=LEGEND_MAX_COLUMNS):
    """Save the current Matplotlib figure at 1200 dpi and store its legend separately."""
    fig = plt.gcf()
    handles = []
    labels = []

    for ax in fig.get_axes():
        h, l = ax.get_legend_handles_labels()
        handles.extend(h)
        labels.extend(l)
        legend = ax.get_legend()
        if legend is not None:
            legend.remove()

    for fig_legend in list(fig.legends):
        legend_handles = getattr(
            fig_legend,
            "legend_handles",
            getattr(fig_legend, "legendHandles", []),
        )
        legend_labels = [item.get_text() for item in fig_legend.get_texts()]
        handles.extend(legend_handles)
        labels.extend(legend_labels)
        fig_legend.remove()

    fig.savefig(
        save_path,
        dpi=STATIC_PLOT_DPI,
        bbox_inches="tight",
        transparent=True,
    )
    save_legend_figure(handles, labels, save_path, ncol=ncol)


