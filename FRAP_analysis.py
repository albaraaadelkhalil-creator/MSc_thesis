def clean_plot_label(label):
    """
    Remove plotting/statistical details from labels so legends stay readable.
    The returned label keeps only the biological/data identity.
    """
    if label is None:
        return "Data"

    clean = str(label)
    clean = re.sub(r"\s*mean\s*±\s*(sem|std)", "", clean, flags=re.IGNORECASE)
    clean = re.sub(r"\s*\(n\s*=\s*\d+\)", "", clean, flags=re.IGNORECASE)
    clean = clean.replace("QC", "").strip()
    clean = re.sub(r"\s+", " ", clean)
    return clean


