def format_float(value, digits=4):
    """Format finite floats safely for printed summaries."""
    if value is None:
        return "NA"
    try:
        if np.isfinite(value):
            return f"{value:.{digits}f}"
    except Exception:
        pass
    return "NA"


def format_value_with_sd(fit, metric, digits=4):
    """
    Format a pooled fit value with its between-trace sample SD when available.
    """
    value_text = format_float(fit.get(metric), digits)
    sd_value = fit.get(f"{metric}_sd", np.nan)

    try:
        if np.isfinite(sd_value):
            return f"{value_text} ± {sd_value:.{digits}f}"
    except Exception:
        pass

    return value_text


