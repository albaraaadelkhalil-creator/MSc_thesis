def clean_fit_result_for_table(fit_result, group_name, region):
    """
    Remove plotting arrays and add identifiers before saving to CSV.
    Works for FRAP recovery and linear trend fits.
    """
    row = {
        "group": group_name,
        "region": region,
    }

