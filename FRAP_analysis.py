def filter_primary_model_candidates(fits):
    """
    Keep only the free models used for primary model selection:
    - single_exp
    - biexp_free

    Constrained models are still fitted and saved to the supplementary CSV,
    but they are not allowed to determine the plotted best model.
    """
    if fits is None:
        return []
    if isinstance(fits, dict):
        fits = [fits]
    return [
        fit for fit in fits
        if fit is not None and fit.get("model") in PRIMARY_MODEL_TYPES_FOR_SELECTION
    ]


def best_fit_by_bic_priority(fits, primary_only=True):
    """
    Select the best fit for plotting.

    By default, this function only considers the primary free models:
    - single_exp
    - biexp_free

    Constrained models are intentionally excluded from plotted-model selection,
    although they remain in the supplementary CSV tables.

    Priority order:
    1. Lowest finite BIC
    2. Lowest finite AICc, if BIC is unavailable
    3. Lowest finite RMSE, if both BIC and AICc are unavailable
    4. First non-None fit as a final fallback
    """
    if fits is None:
        return None

    if isinstance(fits, dict):
        fits = [fits]

    valid_fits = [fit for fit in fits if fit is not None]
    if len(valid_fits) == 0:
        return None

    if primary_only:
        primary_fits = filter_primary_model_candidates(valid_fits)
        if len(primary_fits) > 0:
            valid_fits = primary_fits
        else:
            print(
                "Warning: no primary free single/bi-exponential fit was available; "
                "falling back to all available models for plotting."
            )

    finite_bic = [fit for fit in valid_fits if np.isfinite(fit.get("bic", np.nan))]
    if finite_bic:
        return min(finite_bic, key=lambda fit: fit["bic"])

    finite_aicc = [fit for fit in valid_fits if np.isfinite(fit.get("aicc", np.nan))]
    if finite_aicc:
        return min(finite_aicc, key=lambda fit: fit["aicc"])

    finite_rmse = [fit for fit in valid_fits if np.isfinite(fit.get("rmse", np.nan))]
    if finite_rmse:
        return min(finite_rmse, key=lambda fit: fit["rmse"])

    return valid_fits[0]
