def plot_comparison_traces_with_best_fits(
    time_axis,
    datasets,
    title,
    ylabel="Normalized intensity",
    save_path=None,
    save_plots=False,
    xlim=None,
    ylim=None,
):
    """
    Plot multiple mean ± error traces and overlay only the BIC-prioritized
    best fitted model for each trace.

    Expected dataset format:
        {
            "data": data_2d,
            "label": "Trace label",
            "fits": [fit1, fit2, ...],
            "fit_linestyle": "-"   # optional
        }

    This helper is used for cross-system comparison plots, for example:
    - Branched versus Linear cortex, with reference traces
    - Branched versus Linear lumen, with reference traces
    - Close-up versions of the same plots
    """
    if datasets is None or len(datasets) == 0:
        print(f"Skipping plot '{title}' because no datasets were provided.")
        return

    plt.figure(figsize=(12, 7))

    # First draw all measured data traces with transparent connected markers.
    for ds in datasets:
        data_2d = np.asarray(ds.get("data"), dtype=float)
        label = ds.get("label", "Data")

        if data_2d.ndim != 2 or data_2d.shape[0] == 0:
            print(f"Skipping dataset '{label}' in plot '{title}' because it is empty or not 2D.")
            continue

        mean, err = compute_error(data_2d, error_type=ERROR_TYPE)

        cont = plt.errorbar(
            time_axis,
            mean,
            yerr=err,
            fmt="o-",
            linestyle="-",
            label=legend_data_label(label, data_2d),
            capsize=3,
            alpha=0.50,
            linewidth=1.6,
            markersize=4,
            zorder=2,
        )

        for barcol in cont[2]:
            barcol.set_alpha(0.22)
        for cap in cont[1]:
            cap.set_alpha(0.22)

    # Then draw all selected fits above the data.
    for ds in datasets:
        data_2d = np.asarray(ds.get("data"), dtype=float)
        label = ds.get("label", "Data")
        fits = ds.get("fits", None)
        fit_linestyle = ds.get("fit_linestyle", "-")

        if data_2d.ndim != 2 or data_2d.shape[0] == 0:
            continue

        best_fit = best_fit_by_bic_priority(fits)
        if best_fit is None:
            continue

        plt.plot(
            best_fit["t_smooth"],
            best_fit["y_smooth"],
            linewidth=3.0,
            linestyle=fit_linestyle,
            label=legend_fit_label(label, data_2d, best_fit),
            zorder=12,
        )

    plt.axhline(1.0, linestyle=":", linewidth=1, zorder=1)
    if len(time_axis) > PREBLEACH_POINTS:
        plt.axvline(time_axis[PREBLEACH_POINTS], linestyle=":", linewidth=1, zorder=1)

    if xlim is not None:
        plt.xlim(*xlim)
    if ylim is not None:
        plt.ylim(*ylim)

    plt.xlabel("Time (s)")
    plt.ylabel(ylabel)
    plt.title(title)
    plt.legend(fontsize=FONT_SIZE, loc="best", prop={"size": FONT_SIZE, "weight": "bold"})
    plt.tight_layout()

    if save_plots and save_path is not None:
        save_current_plot_without_legend(save_path)

    plt.show()
    plt.close()


def save_legend_as_separate_file(ax, legend_save_path, ncol=1, fontsize=FONT_SIZE):
    """
    Save the current axes legend as a separate PNG file.

    This keeps the main figure uncluttered while preserving the full legend
    for thesis assembly or figure layout editing.
    """
    if legend_save_path is None:
        return

    handles, labels = ax.get_legend_handles_labels()
    if len(handles) == 0:
        return

    legend_fig = plt.figure(figsize=(7.5, max(1.2, 0.42 * len(labels))))
    legend_ax = legend_fig.add_subplot(111)
    legend_ax.axis("off")

    legend_ax.legend(
        handles,
        labels,
        loc="center left",
        frameon=False,
        fontsize=fontsize,
        prop={"size": fontsize, "weight": "bold"},
        ncol=ncol,
    )

    legend_fig.tight_layout()
    legend_fig.savefig(legend_save_path, dpi=STATIC_PLOT_DPI, bbox_inches="tight", transparent=True)
    plt.close(legend_fig)
    print(f"Separate legend saved:\n{legend_save_path}")


