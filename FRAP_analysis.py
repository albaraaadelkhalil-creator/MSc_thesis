def reference_fading_fraction(reference_norm, prebleach_points=5):
    """
    Estimate total acquisition bleaching/fading in a reference ROI.

    reference_norm should already be background-corrected and normalized to prebleach.
    """
    reference_norm = np.asarray(reference_norm, dtype=float)

    start_level = np.nanmean(reference_norm[:prebleach_points])
    end_level = np.nanmean(reference_norm[-5:])

    fading_fraction = 1.0 - (end_level / start_level)

    return fading_fraction

